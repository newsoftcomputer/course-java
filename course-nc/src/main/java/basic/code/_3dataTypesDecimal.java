package basic.code;

public class _3dataTypesDecimal {
    public static void main(String[] args) {

        // DATA TYPES - PRIMITIVES- DECIMALS

        // FLOAT
        // Space: 4 Bytes
        // Range: Approx 6 or 7 decimal numbers
        // Suffix: F
        Float valFloat = 5.13458F;
        System.out.println("Float: " + valFloat);

        // DOUBLE
        // Space: 8 Bytes
        // Range: Approx 15 decimal numbers
        Double valDouble = 5.1874565798465;
        System.out.println("Double: " + valDouble);
    }
}
